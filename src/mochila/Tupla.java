package mochila;

public class Tupla {
	private double x , y;
	public Tupla(double A, double B){
		this.x = A;
		this.y = B;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
